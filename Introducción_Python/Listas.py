#Lista
list_things=[60, 7.8, 'String', True]
print(list_things)
print(list_things[0])
print(list_things[-1]) #Imprimir el último elemento

#Slicing
print(list_things[0:2]) #Incluye uno anterior al índice indicado
print(list_things[:2]) #Va desde la posición 0 hasta la indicada
print(list_things[1:])

