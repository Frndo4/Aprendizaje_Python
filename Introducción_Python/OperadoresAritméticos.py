#Operadores Aritméticos

#Suma
n1 = 3
n2 = 5
r = n1+n2
print(r)

#Resta
n1 = 3
n2 = 5
r = n1-n2
print(r)

#Negación
n1 = -3
n2 = 5
r = n1
print(r)

#Multiplicación
n1 = 3
n2 = 5
r = n1*n2
print(r)

#Potencia
n1 = 3
r = n1**2
print(r)

#División
n1 = 3
n2 = 5
r = n1/n2
print(r)

#División entera
n1 = 3
n2 = 5
r = n1//n2
print(r)

#Módulo
n1 = 3
n2 = 5
r = n1%n2
print(r)

#Ejemplo
r = (5+(9/3))**2
print(r)